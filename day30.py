S1 = {"Red", "yellow", "orange", "blue"}         
S2 = {"violet", "blue", "purple"}

print("Set S1:", S1)
print("Set S2:", S2)

# Union
print("Union (S1 | S2):", S1 | S2)

# Intersection
print("Intersection (S1 & S2):", S1 & S2)

# Difference
print("S1 - S2:", S1 - S2)
print("S2 - S1:", S2 - S1)

# Symmetric Difference
print("Symmetric Difference (S1 ^ S2):", S1 ^ S2)










